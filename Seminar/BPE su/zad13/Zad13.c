/* 13.	Учебна зала има правоъгълен размер w на h метра. Залата е разделена на две части – лява и дясна,
с коридор приблизително по средата. В лявата и в дясната част има редици с бюра. В задната част на залата има
голяма входна врата. В предната част на залата има катедра с подиум за преподавателя. Едно работно място
заема 70 на 120 cm. Коридорът е широк поне 100 cm. Изчислено е, че заради входната врата се губи
точно 1 работно място, а заради катедрата се губят точно 2 работни места. Напишете програма, която прочита
размерите на учебната зала и изчислява броя работни места в нея при описаното разположение.
вход: 15 8,9   изход: 129
вход 8,4 5,2 изход: 39 */

#include <stdio.h>

int main()
{
    double w, h;

    if (scanf("%lf %lf", &w, &h) != 2) {
        return 1;
    }

    int length_cm = (int)(w * 100 + 0.1);
    int width_cm = (int)(h * 100 + 0.1);

    int rows = length_cm / 120;

    int desks_per_row = (width_cm - 100) / 70;

    int total_places = (rows * desks_per_row) - 3;

    printf("%d\n", total_places);

    return 0;
}